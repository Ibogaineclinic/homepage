                <h1><strong>Ibogaine Dosage and Treatment</strong></h1> For the safety of our patients, all potential customers must be screened by our medical staff before being approved for<a href="http://ibogaineclinic.com/ibogaine/">Ibogaine</a>treatment.  This also allows us to prepare a treatment that fits perfectly with your condition and needs. Your Ibogaine treatment will begin upon your arrival at our Playa Del Carmen Resort Facility in Mexico. Most patients will need to allocate 5-7 days for the treatment, though many may need longer treatments.  The length of treatment will depend on the history of the addictive behavior.
                <h4>Day One and Two – Orientation and Relaxation</h4> Day One and most of Day Two are devoted to patient relaxation and orientation. You’ll be able to settle in and spend about two hours finalizing paperwork and enjoying our informative orientation process. If needed, patients will be provided with prescription medications to treat the symptoms of withdrawal. Any other medical needs that arise will also be addressed. The patient needs to be comfortable and stabilized in preparation for the Ibogaine treatment.  The beginning of the treatment is targeted for the evening of Day Two. While the end of Day Two is the ideal time for the Ibogaine treatment, it may be delayed in favor of patient safety and comfort.  All of these variables can make a difference when it comes to treatment effectiveness.
                <h4>Evening of Day Two – Administration of Ibogaine</h4> By the end of Day Two, the patient should ideally being experiencing withdrawal symptoms.  These symptoms, when paired with the<a href="http://ibogaineclinic.com/ibogaine/">Ibogaine</a>treatment, increase the effectiveness of the drug. Ibogaine is usually administered in capsule form, traditionally in doses of 500-800mg, depending on the body weight of the patient. Here at this facility, we have created a host of unique and customized protocols and will prescribe one of these based on your intake screening. These protocols may spread out the Ibogaine dosage over a period of time. Most patients will feel the effect of the drugs within about one hour, and they will usually last for 36-48 hours. This takes up all of Day Three and most of Day Four.
                <h4>Days Three and Four – Introspection, Forgiveness, Healing, and Freedom from Addiction</h4> During the entire Ibogaine treatment, the patient will be in a darkened, quiet room. Most patients generally do not want food during the experience.  Patients will be constantly monitored by our trained medical staff throughout the entire treatment, and are able to offer any help necessary.  They will also encourage hydration, as many patients forget to drink during the experience as well. As Ibogaine interferes with the patient’s ability to move around, they may find it is difficult to get to the bathroom. While the Ibogaine is active, the patient will find themselves in a deep and introspective state. Most will be able to reflect on past events with a clarity and truth they could not have otherwise obtained. This allows the patients to forgive themselves and others, as well as to let go of any resentments and hatred to which they may still cling. By removing these thoughts and emotions, patients are able to take the first steps toward freeing themselves from addiction. Removing these negative emotions allows the neurotransmitters in the patient’s brain to reset. This new freedom from an emotional past allows the patient to look to a new future, one full of opportunities and free of drug addiction and destructive tendencies.
                <h4>Day 5-7: The Beginning of a New Life</h4> The Ibogaine experience will usually wear off late on Day Four or early on Day Five.  Once the effects have worn off completely, patients are encouraged to call friends, family, and loved ones.  These calls promote a feeling of connectedness and belonging. To prevent additional stresses, it is recommended that these patients do not discuss any major life decisions until all the short term effects of the Ibogaine treatment have worn off. This usually takes several days. After the primary effects of the treatment have worn off, the patient will be able to consult with one of our trained Diet Coordinators.  Additionally, patients will be encouraged to begin conversations with our counseling staff.  All of these steps help to increase the effectiveness of the Ibogaine treatment. Most of Day Five is used to allow the patient to regain physical and emotional control and stability. Many patients find that they rediscover a zest for life that was lost during the course of their addiction. Before leaving the facility, the patient needs to discuss options with the Activities Coordinator.  One of the most popular and highly recommended activities is the “victory hike” which will be scheduled for the afternoon before the patient’s departure. This event is designed for the patient to reflect on their experience at the facility, as well as to provide some time to experience the joy and beauty of nature. Primarily, though, it gives the patient the opportunity to complete their first challenge as a person without addiction. This activity shows the patient that they are a new person with unlimited potential. The primary goal of all drug treatment programs is to provide empowerment, and this hike is designed to do just that.
                <h4>Departure</h4> Upon their departure, patients will be given a care package full of mementos and other items to help them remain true to their decision to remain free from addiction.  They will also be given a journal full of personal notes and encouragement from the staff members, in which they can record their own thoughts. Most patients, once they have recovered from the immediate effects of the Ibogaine, are able to leave the facility on the morning of Day Five. Many prefer to stay for additional time to enjoy their new lease on life in beautiful Mexico.  We encourage patients to stay as long as they feel is necessary, and most will stay for at least seven days.
                <div class="col col-xs-12 col-md-6">
                    <h2><strong>After Ibogaine Treatment</strong></h2> Once the patient returns to his or her previous life, they will be frequently contacted by staff members from<a href="http://ibogaineclinic.com/ibogaine/">Ibogaine</a>by David Dardasti. This contact will become less frequent as the patient resumes his or her normal life and activities.  Patients are encouraged to reach out to staff members as often as needed, and the staff will do whatever is needed (and reasonable) to prevent the patient from relapsing into addiction. Staff members keep a copy of each patient’s treatment goals handy at the clinic.  This enables the staff members to help patients with their recovery. This help is vital for the maintenance of a healthy life. People who have already completed their Ibogaine treatments often wish to get involved with helping others through their treatment processes.  This is a healthy and in many cases vital part of the recovery process. It helps the patients to regain a sense of purpose, and to rebuild their sense of self-worth. Our facility maintains a constantly updated database of former and potential patients.  We can give referrals to help with networking – a system designed to help patients in need.  In the interest of privacy, names and phone numbers are only given out after written consent forms are obtained. If you have any questions please feel free to call us at 1 888-462-1164 or send us an email directly through our website. We are happy to answer any and all questions you may have.
